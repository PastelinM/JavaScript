/**
 * ¿Qué es  el DOM?
 * El DOM (Document Object Model, en español Modelo de Objetos del Documento).
 * El DOM es un modelo de documento que se carga en el navegador web y que representa el documento como un árbol de nodos, 
 * en donde cada nodo representa una parte del documento (puede tratarse de un elemento, una cadena de texto o un comentario).
 */