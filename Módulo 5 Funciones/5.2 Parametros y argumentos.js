/**
 * @parametros
 * Son variables mencionadas en la definición de una función. Son nombres que se utilizan dentro de la función y actuan como contenedores para los valores que se pasan cuando la función es llamada.
 * 
 */

/**
 * @ejemplo
 * Nota: a y b son parámetros de la función suma.
 */

function suma(a, b) {
    return a + b;
}


/**
 * @argumentos
 * Son los valores reales pasados a la función cuando esta es invocada. Son valores que se asignan a los 
 * parámetros de la función cuando se llama a esa función.
 */


/**
 * @ejemplo
 * Nota: En esta llamada a la función suma, 3 y 5 son los argumentos que se asignarán a a y b 
 * respectivamente dentro de la función.
 */

suma(3, 5);



