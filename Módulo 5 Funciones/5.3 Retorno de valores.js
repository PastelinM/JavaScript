/**
 * @retorno
 * Se refiere a la capacidad de una función para devolver un resultado depués de 
 * realiazar sus operaciones. Esto se logra mediante la plabra clave "return".
 * @ejemplo
 */

function suma(a, b) {
    return a + b;
}

let resultado = suma(3, 4); // Llama a la función 'suma' con los argumentos 3 y 4
console.log(resultado); // Imprime 7, el valor devuelto por la función 'suma'
