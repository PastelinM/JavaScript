/**
 * @operadoresAritmeticos
 *Se utilizan para realizar operaciones matemáticas en valores numéricos. Aquí están los operadores aritméticos disponibles en Js.
 */

/**
 * @suma
 * Se utiliza para sumar dos valores
 * Resultado:8
 */
const suma = 5 + 3; 

/**
 * @resta
 * Se utiliza para restar un valor de otro
 * Resultado:6
 */
const resta = 10 - 4;

/**
 * @multiplicación
 * Se utiliza para multiplicar dos valores
 * Resultado:42
 */
const multiplicacion = 6* 7;

/**
 * @división
 * Se utiliza para dividir un valor entre otro
 * Resultado:4
 */
const division = 20 / 5;

/**
 * @módulo
 * Devuelve el residuo de la división entre dos valores
 * Nota: Resultado: 2 (17 / 5 = 3 con un residuo de 2)
 */

const modulo = 17 % 5; 

/**
 * @incremento
 * Aumenta en uno el valor de una variable
 * Nota: Resultado: 11
 */
let incremento = 10;
incremento ++;

/**
 * @decremento
 * Reduce en uno el valor de una variable
 *Nota: Resultado: 14
 */

let decremento = 15;
decremento --;

/**
 *@OperadorCompuesto
 *Estos operadores combinan una operación aritmetica con una asignación incluyen (+=,-=,*=,/=)
 *Nota: Resultado: 8
 */

 let valor = 5;
 valor += 3;
